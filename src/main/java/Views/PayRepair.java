    //fetch payment details from repair table to the ui
    public void getPaymentDetails() {

        repair_id = Integer.parseInt(txt_repairid.getText());

        try {
            Connection con = DBConnection.getConnection();
            String sql = "select price, amount_paid, amount_due from repair where repair_id = ?;";
            PreparedStatement pst = con.prepareStatement(sql);

            pst.setInt(1, repair_id);
            ResultSet rs = pst.executeQuery();

            while (rs.next()) {
                txt_totalCost.setText(rs.getString("price"));
                txt_amountpaid.setText(rs.getString("amount_paid"));
                txt_amountDue.setText(rs.getString("amount_due"));
            }

        } catch (Exception e) {
            e.printStackTrace();
        }

    }

    //update new payment details to the table
    public boolean updateRepairPayment() {
        boolean update = false;
        repair_id = Integer.parseInt(txt_repairid.getText());
        totalCost = Double.parseDouble(txt_totalCost.getText());
        amountPaid = Double.parseDouble(txt_amountpaid.getText());
        amountDue = Double.parseDouble(txt_amountDue.getText());
        pay = Double.parseDouble(txt_pay.getText());

        try {
            Connection con = DBConnection.getConnection();
            String sql = "update repair set price = ?, amount_paid = ?, amount_due = ? where repair_id = ?;";
            PreparedStatement pst = con.prepareStatement(sql);

            pst.setDouble(1, totalCost);
            pst.setDouble(2, amountPaid);
            pst.setDouble(3, amountDue);
            pst.setInt(4, repair_id);

            int rowCount = pst.executeUpdate();
            if (rowCount > 0) {
                update = true;
            } else {
                update = false;
            }
        } catch (Exception e) {
            e.printStackTrace();
        }

        return update;
    }

