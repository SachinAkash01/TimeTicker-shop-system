    private void rSMaterialButtonCircle1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_rSMaterialButtonCircle1ActionPerformed
        if (updateRepairPayment() == true) {
            double cost = Double.parseDouble(txt_totalCost.getText());
            double pay = Double.parseDouble(txt_pay.getText());

            try {
                Connection con = DBConnection.getConnection();
                String sql1 = "insert into income(income, date) values(?,curdate());";
                PreparedStatement pst1 = con.prepareStatement(sql1);
                
                pst1.setDouble(1, pay);
                pst1.executeUpdate();
            } catch (Exception e) {
                e.printStackTrace();
            }
            
            double amount = Double.parseDouble(txt_amountpaid.getText()) + pay;
            double due = cost - amount;

            txt_amountDue.setText(String.valueOf(due));
            txt_amountpaid.setText(String.valueOf(amount));
            JOptionPane.showMessageDialog(this, "Payment Details Updated Successfully!");
            updateRepairPayment();
        } else {
            JOptionPane.showMessageDialog(this, "Payment Details Updation Failed!");
        }
    }//GEN-LAST:event_rSMaterialButtonCircle1ActionPerformed

