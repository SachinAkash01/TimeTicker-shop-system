    private void Update_table() {

        try {
            Connection con = DBConnection.getConnection();
            Statement st = con.createStatement();
            String sql = "select * from invoice";
            //PreparedStatement prep = con.prepareStatement(sql);
            ResultSet rs = st.executeQuery(sql);

            //tbl_maintable.setModel(DbUtils.resultSetToTableModel(rs));
            while (rs.next()) {
                String inid = rs.getString("invoiceID");
                String cusname = rs.getString("cusname");
                String cusid = rs.getString("cusNIC");
                String brand = rs.getString("Brand");
                String color = rs.getString("Color");
                String snum = rs.getString("SNum");
                String price = rs.getString("Price");
                String pamount = rs.getString("PaidAmount");
                String balance = rs.getString("Balance");
                String pmethod = rs.getString("PaymentMethod");
                String wtype = rs.getString("WatchType");

                Object[] obj = {inid, cusname, cusid, brand, color, snum, pmethod, wtype, price, pamount, balance};
                model = (DefaultTableModel) tbl_maintable.getModel();
                model.addRow(obj);
            }

        } catch (Exception e) {
            e.printStackTrace();
            //JOptionPane.showMessageDialog(null, e);

        }

    }

    public void clearTable() {
        DefaultTableModel model = (DefaultTableModel) tbl_maintable.getModel();
        model.setRowCount(0);
    }

