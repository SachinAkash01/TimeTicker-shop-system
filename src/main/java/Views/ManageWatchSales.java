    private void tbl_maintableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_maintableMouseClicked
        // TODO add your handling code here:

        int row = tbl_maintable.getSelectedRow();
        String Table_click = (tbl_maintable.getModel().getValueAt(row, 0).toString());

        try {
            Connection con = DBConnection.getConnection();
            String sql = "select * from invoice where invoiceID=' " + Table_click + " '  ";
            PreparedStatement prep = con.prepareStatement(sql);
            ResultSet rs = prep.executeQuery();

            if (rs.next()) {
                String invoiceID = rs.getString("invoiceID");
                txtID.setText(invoiceID);

                String cusname = rs.getString("cusname");
                txt_cusname.setText(cusname);

                String cusNIC = rs.getString("cusNIC");
                txtCusNIC.setText(cusNIC);

                String Brand = rs.getString("Brand");
                txtBrand.setText(Brand);

                String Color = rs.getString("Color");
                txtColor.setText(Color);

                String SNum = rs.getString("SNum");
                txtSNum.setText(SNum);

                String Price = rs.getString("Price");
                txtPrice.setText(Price);

                String Paid = rs.getString("PaidAmount");
                txtPaidAmount.setText(Paid);

                String Balance = rs.getString("Balance");
                txtBalance.setText(Balance);

                //================================================
                String PaymentMethod = rs.getString("PaymentMethod");
                if ("Cash".equals(PaymentMethod)) {
                    rdoCash.setSelected(true);
                }
                if ("Card".equals(PaymentMethod)) {
                    rdoCards.setSelected(true);

                }

                //===========================================
                String WatchType = rs.getString("WatchType");
                if ("Smart".equals(WatchType)) {
                    rdoSmart.setSelected(true);
                }

                //===========================================
                if ("Analog".equals(WatchType)) {
                    rdoAnalog.setSelected(true);
                }

                //===========================================
                if ("Digital".equals(WatchType)) {
                    rdoDigital.setSelected(true);
                }
                //===========================================
            }
        } catch (Exception e) {
            e.printStackTrace();
        }


    }//GEN-LAST:event_tbl_maintableMouseClicked

    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:

        try {
            Connection con = DBConnection.getConnection();
            String sql = "INSERT INTO invoice(cusname,cusNIC,Brand,Color,SNum,Price,PaidAmount,Balance,PaymentMethod,WatchType)" + "values(?,?,?,?,?,?,?,?,?,?)";
            String sql1 = "insert into income(income, date) values(?,curdate());";
            PreparedStatement prep = con.prepareStatement(sql);
            PreparedStatement pst1 = con.prepareStatement(sql1);

            prep.setString(1, txt_cusname.getText().trim());
            prep.setString(2, txtCusNIC.getText().trim());
            prep.setString(3, txtBrand.getText().trim());
            prep.setString(4, txtColor.getText().trim());
            prep.setString(5, txtSNum.getText().trim());
            prep.setDouble(6, Double.parseDouble(txtPrice.getText()));
            prep.setDouble(7, Double.parseDouble(txtPaidAmount.getText()));
            prep.setDouble(8, Double.parseDouble(txtBalance.getText()));
            prep.setString(9, PaymentMethod);
            prep.setString(10, WatchType);

            prep.execute();

            pst1.setDouble(1, Double.parseDouble(txtPrice.getText().trim()));
            pst1.executeUpdate();

            clearTable();
            Update_table();
            JOptionPane.showMessageDialog(this, "Successfully added to the database");
        } catch (Exception e) {
            e.printStackTrace();
            //JOptionPane.showMessageDialog(null,e);
        }


    }//GEN-LAST:event_btnAddActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:

        if (txtID.getText().equals("invoiceID")) {
            JOptionPane.showMessageDialog(null, "Please Select The Data Which You Need To Delete");
        } else {
            int p = JOptionPane.showConfirmDialog(null, "Please Confirm To Delete", "Delete", JOptionPane.YES_NO_OPTION);
            if (p == 0) {
                try {
                    Connection con = DBConnection.getConnection();
                    String sql = "delete from invoice where invoiceID=?";
                    PreparedStatement prep = con.prepareStatement(sql);

                    prep.setString(1, txtID.getText());
                    prep.execute();

                    JOptionPane.showMessageDialog(null, "Successfully Deleted");

                } catch (Exception e) {
                    e.printStackTrace();
                    //JOptionPane.showMessageDialog(null, e);
                }
            }

            clearTable();
            Update_table();
        }


    }//GEN-LAST:event_btnDeleteActionPerformed

