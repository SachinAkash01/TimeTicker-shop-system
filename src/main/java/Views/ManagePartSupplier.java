    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
        // TODO add your handling code here:

        if (txtSupCodeNo.getText().equals("SComCode")) {
            JOptionPane.showMessageDialog(null, "Please Select The Data Which You Need To Delete");
        } else {
            int p = JOptionPane.showConfirmDialog(null, "Please Confirm To Delete", "Delete", JOptionPane.YES_NO_OPTION);
            if (p == 0) {
                try {
                    DBConnection con = new DBConnection();
                    String sql = "delete from managepartsupp where SComCode=?";
                    PreparedStatement prep = con.prepareStatement(sql);

                    prep.setString(1, txtSupCodeNo.getText());
                    prep.execute();

                    JOptionPane.showMessageDialog(null, " Deleted");

                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, e);
                }
            }

            clearTable();
            Updatetable();
            ClearButton();
        }

    }//GEN-LAST:event_btnRemoveActionPerformed

