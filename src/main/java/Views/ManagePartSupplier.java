    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:
        try {

            DBConnection con = new DBConnection();
            String sql = "UPDATE managepartsupp set SComName=? , ComContact=? , ComEmailAdd=? , total=? , AmountToPaid=? , AmountDue=? ,  ItemCode=? , ItemName=? , ItemQuant=? , ItemSerial=? , ItemCondition=? WHERE SComCode=?";

            PreparedStatement prep = con.prepareStatement(sql);

            prep.setString(1, txtSupCompany.getText());
            prep.setString(2, txtCompanyConNumber.getText());
            prep.setString(3, txtCompEmailAdd.getText());
            prep.setInt(4, Integer.parseInt(txtTotal.getText().trim()));
            prep.setInt(5, Integer.parseInt(txtAmntToPaid.getText().trim()));
            prep.setInt(6, Integer.parseInt(txtAmntDue.getText().trim()));
            prep.setInt(7, Integer.parseInt(txtItemCode.getText().trim()));
            prep.setString(8, txtItemName.getText());
            prep.setInt(9, Integer.parseInt(txtItemQuantity.getText().trim()));
            prep.setString(10, txtItemSerialNum.getText());
            prep.setString(11, itemCondition);
            prep.setString(12, txtSupCodeNo.getText());

            prep.execute();
            JOptionPane.showMessageDialog(this, "Edit Saved");

        } catch (Exception e) {

            JOptionPane.showMessageDialog(null, e);
        }

        clearTable();
        Updatetable();
        ClearButton();

    }//GEN-LAST:event_btnEditActionPerformed

