                String ComEmailAdd = r.getString("ComEmailAdd");
                txtCompEmailAdd.setText(ComEmailAdd);

                String total = r.getString("total");
                txtTotal.setText(total);

                String AmountToPaid = r.getString("AmountToPaid");
                txtAmntToPaid.setText(AmountToPaid);

                String AmountDue = r.getString("AmountDue");
                txtAmntDue.setText(AmountDue);

                String ItemCode = r.getString("ItemCode");
                txtItemCode.setText(ItemCode);

                String ItemName = r.getString("ItemName");
                txtItemName.setText(ItemName);

                String ItemQuant = r.getString("ItemQuant");
                txtItemQuantity.setText(ItemQuant);

